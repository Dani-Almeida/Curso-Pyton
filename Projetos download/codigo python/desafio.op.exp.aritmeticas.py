numero1 = float(input("Por favor insira o primeiro número com casas decimais "))
numero2 = float(input("Agora, insira o segundo número com casas decimais "))
numero3 = float(input("Por fim, insira o terceiro número com casas decimais "))

media = (numero1 + numero2 + numero3) / 3

print(f"A média dos números é: {media: .2f}")
